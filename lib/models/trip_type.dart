      '–¥–æ–Ω–µ—Ü–∫-–∫—É—Ç–µ–π–Ω–∏–∫–æ–≤–æ', '–∫—É—Ç–µ–π–Ω–∏–∫–æ–≤–æ-–¥–æ–Ω–µ—Ü–∫',
