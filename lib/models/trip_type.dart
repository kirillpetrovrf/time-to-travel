    double t = (px_km * dx + py_km * dy) / lengthSq;
    t = t < 0 ? 0 : (t > 1 ? 1 : t); // –æ–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º t –≤ –ø—Ä–µ–¥–µ–ª–∞—Ö [0, 1]
    
