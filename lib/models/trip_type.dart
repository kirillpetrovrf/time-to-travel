      '–≥–æ—Ä–æ–¥ —Ä–æ—Å—Ç–æ–≤-–Ω–∞-–¥–æ–Ω—É',
    ];
    
    final isFromDonetsk = donetskVariants.any((variant) => fromLower.contains(variant));
    final isToRostov = rostovExactMatches.any((variant) => toLower.contains(variant));
    
    return isFromDonetsk && isToRostov;
  }

  /// üÜï –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –º–∞—Ä—à—Ä—É—Ç –ª–æ–∫–∞–ª—å–Ω—ã–º —Ç—Ä–µ—É–≥–æ–ª—å–Ω—ã–º –º–µ–∂–¥—É –î–æ–Ω–µ—Ü–∫–æ–º, –ú–∞–∫–µ–µ–≤–∫–æ–π –∏ –•–∞—Ä—Ü—ã–∑—Å–∫–æ–º
  /// –õ–æ–∫–∞–ª—å–Ω—ã–µ –º–∞—Ä—à—Ä—É—Ç—ã –∏–º–µ—é—Ç —Ü–µ–Ω—É 8,000‚ÇΩ –∏ –ù–ï –ø—Ä–æ—Ö–æ–¥—è—Ç —á–µ—Ä–µ–∑ –ö–ü–ü –ê–≤–µ–ª–æ–Ω-–£—Å–ø–µ–Ω–∫–∞
