    final projX = t * dx;
    final projY = t * dy;
    
    // –†–∞—Å—Å—Ç–æ—è–Ω–∏–µ –æ—Ç —Ç–æ—á–∫–∏ –¥–æ –ø—Ä–æ–µ–∫—Ü–∏–∏
    return _distance(px_km, py_km, projX, projY);
  }

