  }

  /// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ø–µ—Ü–∏–∞–ª—å–Ω—É—é —Ü–µ–Ω—É –¥–ª—è –º–∞—Ä—à—Ä—É—Ç–∞ –î–æ–Ω–µ—Ü–∫-–†–æ—Å—Ç–æ–≤ –Ω–µ–∑–∞–≤–∏—Å–∏–º–æ –æ—Ç —Ä–∞—Å—Å—Ç–æ—è–Ω–∏—è
